const state = {
  // Les preferences sont gardees entre deux visites.
  lang: localStorage.getItem("portfolio-lang") || "fr",
  theme: localStorage.getItem("portfolio-theme") || "dark"
};

const menuToggle = document.querySelector(".menu-toggle");
const mainNav = document.querySelector(".main-nav");
const langToggle = document.querySelector(".lang-toggle");
const themeToggle = document.querySelector(".theme-toggle");
const navLinks = [...document.querySelectorAll(".nav-link")];
const sections = [...document.querySelectorAll("main section[id]")];
const revealItems = [
  ...document.querySelectorAll(
    ".section-title, .about-copy, .languages-card, .skill-card, .soft-skills, .project-card, .timeline-card, .contact-panel"
  )
];

const iconSets = {
  // Noms des icones Lucide ajoutees automatiquement dans l'interface.
  nav: {
    accueil: "home",
    apropos: "user-round",
    competences: "code-2",
    projets: "folder-kanban",
    contact: "send"
  },
  soft: {
    "soft.team": "users",
    "soft.org": "list-checks",
    "soft.fast": "zap",
    "soft.com": "message-circle",
    "soft.time": "clock"
  },
  projects: ["sparkles", "bot", "scan-line", "monitor-cog"],
  languages: ["languages", "book-open-text", "message-square-text"]
};

const progressBar = document.createElement("div");
// La barre est creee en JavaScript pour eviter d'ajouter du HTML repetitif.
progressBar.className = "scroll-progress";
document.body.prepend(progressBar);

function renderIcons() {
  // Convertit les balises data-lucide en SVG.
  if (window.lucide) {
    window.lucide.createIcons();
  }
}

function addIcon(element, iconName, className = "auto-icon") {
  if (!element || element.querySelector(`.${className}`)) return;

  const icon = document.createElement("i");
  icon.dataset.lucide = iconName;
  icon.className = className;
  icon.setAttribute("aria-hidden", "true");
  element.prepend(icon);
}

function enhanceIcons() {
  // Ajoute les icones sans modifier manuellement chaque bloc HTML.
  navLinks.forEach((link) => addIcon(link, iconSets.nav[link.dataset.page]));

  document.querySelectorAll(".soft-skills span[data-i18n]").forEach((item) => {
    addIcon(item, iconSets.soft[item.dataset.i18n]);
  });

  document.querySelectorAll(".project-card h3").forEach((title, index) => {
    addIcon(title, iconSets.projects[index] || "folder");
  });

  document.querySelectorAll(".language-row div").forEach((row, index) => {
    if (row.querySelector(".language-label")) return;
    const strong = row.querySelector("strong");
    if (!strong) return;

    const label = document.createElement("span");
    label.className = "language-label";
    addIcon(label, iconSets.languages[index] || "languages");
    label.append(strong);
    row.prepend(label);
  });

  renderIcons();
}

function applyLanguage() {
  // Remplace tous les textes qui possedent un attribut data-i18n.
  document.documentElement.lang = state.lang;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[state.lang][key] || element.textContent;
  });
  langToggle.querySelector("span").textContent = state.lang === "fr" ? "EN" : "FR";
  localStorage.setItem("portfolio-lang", state.lang);
  enhanceIcons();
}

function applyTheme() {
  // Active le theme clair/sombre et met a jour l'icone du bouton.
  document.body.classList.toggle("light", state.theme === "light");
  themeToggle.innerHTML = state.theme === "light" ? '<i data-lucide="moon"></i>' : '<i data-lucide="sun"></i>';
  localStorage.setItem("portfolio-theme", state.theme);
  renderIcons();
}

function updateScrollProgress() {
  // Calcule le pourcentage parcouru dans la page.
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const progress = scrollable > 0 ? (window.scrollY / scrollable) * 100 : 0;
  progressBar.style.width = `${Math.min(progress, 100)}%`;
}

function showWelcomeToast() {
  // Message de bienvenue personnalise, plus agreable qu'une alert navigateur.
  const toast = document.createElement("div");
  toast.className = "welcome-toast";
  toast.innerHTML = `
    <span class="welcome-icon"><i data-lucide="sparkles"></i></span>
    <div>
      <strong>Bienvenue !</strong>
      <p>Merci de visiter le portfolio de Ali Ahnach.</p>
    </div>
    <button class="welcome-close" type="button" aria-label="Fermer"><i data-lucide="x"></i></button>
  `;

  document.body.append(toast);
  renderIcons();

  window.setTimeout(() => toast.classList.add("show"), 300);
  window.setTimeout(() => toast.classList.remove("show"), 5200);
  window.setTimeout(() => toast.remove(), 5700);

  toast.querySelector(".welcome-close").addEventListener("click", () => {
    toast.classList.remove("show");
    window.setTimeout(() => toast.remove(), 350);
  });
}

menuToggle.addEventListener("click", () => {
  const isOpen = mainNav.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  menuToggle.innerHTML = isOpen ? '<i data-lucide="x"></i>' : '<i data-lucide="menu"></i>';
  renderIcons();
});

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    mainNav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    menuToggle.innerHTML = '<i data-lucide="menu"></i>';
    renderIcons();
  });
});

langToggle.addEventListener("click", () => {
  state.lang = state.lang === "fr" ? "en" : "fr";
  applyLanguage();
});

themeToggle.addEventListener("click", () => {
  state.theme = state.theme === "dark" ? "light" : "dark";
  applyTheme();
});

window.addEventListener("scroll", updateScrollProgress, { passive: true });

const observer = new IntersectionObserver(
  // Met en surbrillance le lien de navbar selon la section visible.
  (entries) => {
    const visible = entries
      .filter((entry) => entry.isIntersecting)
      .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];

    if (!visible) return;

    navLinks.forEach((link) => {
      link.classList.toggle("active", link.getAttribute("href") === `#${visible.target.id}`);
    });
  },
  {
    rootMargin: "-35% 0px -50% 0px",
    threshold: [0.1, 0.35, 0.6]
  }
);

const revealObserver = new IntersectionObserver(
  // Declenche les animations d'apparition quand les elements entrent dans l'ecran.
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("visible");
      revealObserver.unobserve(entry.target);
    });
  },
  {
    rootMargin: "0px 0px -12% 0px",
    threshold: 0.15
  }
);

sections.forEach((section) => observer.observe(section));

revealItems.forEach((item, index) => {
  item.classList.add("reveal");
  item.style.setProperty("--delay", `${Math.min(index * 70, 280)}ms`);
  revealObserver.observe(item);
});

document.getElementById("year").textContent = new Date().getFullYear();
applyLanguage();
enhanceIcons();
applyTheme();
renderIcons();
updateScrollProgress();

window.addEventListener("load", () => {
  showWelcomeToast();
});
